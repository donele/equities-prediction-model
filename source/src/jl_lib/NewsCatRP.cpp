int NewsCatRP::cat2i(string cat)
{
	if( m_.count(cat) )
		return m_[cat];
	return -1;
}

string NewsCatRP::i2cat(int i)
{
	for( map<string, int>::iterator it = m_.begin(); it != m_.end(); ++it )
	{
		if( i == it->second )
			return it->first;
	}
	return "";
}

